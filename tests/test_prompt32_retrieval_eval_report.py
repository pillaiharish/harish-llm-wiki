class TestReportDeterminism:
    """Test 4-5: two back-to-back builds produce identical output."""

    def test_two_builds_produce_identical_report(self, tmp_path, monkeypatch):
        rec = _make_pdf_record(tmp_path)
        builder_a = _setup_site_builder(tmp_path / "a", monkeypatch)
        builder_a.build([rec])
        report_a = (builder_a.repo_site_dir / REPORT_REL).read_text(
            encoding="utf-8"
        )

        builder_b = _setup_site_builder(tmp_path / "b", monkeypatch)
        builder_b.build([rec])
        report_b = (builder_b.repo_site_dir / REPORT_REL).read_text(
            encoding="utf-8"
        )

        assert report_a == report_b

    def test_report_contains_no_timestamps(self, tmp_path, monkeypatch):
        """The page is deterministic; no wall-clock timestamps appear."""
        rec = _make_pdf_record(tmp_path)
        builder = _setup_site_builder(tmp_path, monkeypatch)
        builder.build([rec])
        content = (builder.repo_site_dir / REPORT_REL).read_text(encoding="utf-8")
        for needle in ("Generated:", "generated_at", "datetime.now", "time.time"):
            assert needle not in content, (
                f"non-deterministic timestamp source in report: {needle!r}"
            )


