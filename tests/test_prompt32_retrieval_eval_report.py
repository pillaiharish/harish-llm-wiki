class TestVerificationScript:
    """Test 16-20: the verify script accepts --site-dir and exits correctly."""

    def test_verify_script_exists(self):
        assert VERIFY_SCRIPT.exists(), f"missing verify script: {VERIFY_SCRIPT}"

    def test_verify_script_exits_0_on_valid_report(
        self, tmp_path, monkeypatch
    ):
        """When the report exists with all required markers, exit 0."""
        rec = _make_pdf_record(tmp_path)
        builder = _setup_site_builder(tmp_path, monkeypatch)
        builder.build([rec])
        site_dir = builder.repo_site_dir

        result = subprocess.run(
            [sys.executable, str(VERIFY_SCRIPT), "--site-dir", str(site_dir)],
            capture_output=True,
            text=True,
            cwd=str(REPO_ROOT),
        )
        assert result.returncode == 0, (
            f"script exited {result.returncode}: "
            f"stdout={result.stdout!r} stderr={result.stderr!r}"
        )
        assert "All retrieval-eval report checks passed" in result.stdout

    def test_verify_script_exits_nonzero_on_missing_report(self, tmp_path):
        """When the report is missing, exit non-zero."""
        result = subprocess.run(
            [sys.executable, str(VERIFY_SCRIPT), "--site-dir", str(tmp_path)],
            capture_output=True,
            text=True,
            cwd=str(REPO_ROOT),
        )
        assert result.returncode != 0
        assert "missing" in (result.stdout + result.stderr).lower()

    def test_verify_script_exits_nonzero_on_weak_report(self, tmp_path):
        """When the report is a stub without required sections, exit non-zero."""
        (tmp_path / "search").mkdir(parents=True, exist_ok=True)
        (tmp_path / "search" / "eval.md").write_text(
            "# Retrieval Eval Report\n\n_No content._\n", encoding="utf-8"
        )
        result = subprocess.run(
            [sys.executable, str(VERIFY_SCRIPT), "--site-dir", str(tmp_path)],
            capture_output=True,
            text=True,
            cwd=str(REPO_ROOT),
        )
        assert result.returncode != 0

    def test_verify_script_reports_section_gaps(self, tmp_path):
        """The script names the missing section in its failure output."""
        (tmp_path / "search").mkdir(parents=True, exist_ok=True)
        (tmp_path / "search" / "eval.md").write_text(
            "# Retrieval Eval Report\n\n", encoding="utf-8"
        )
        result = subprocess.run(
            [sys.executable, str(VERIFY_SCRIPT), "--site-dir", str(tmp_path)],
            capture_output=True,
            text=True,
            cwd=str(REPO_ROOT),
        )
        assert result.returncode != 0
        # The script should report missing sections, not just a generic error.
        assert "section" in (result.stdout + result.stderr).lower()


