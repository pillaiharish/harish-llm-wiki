"""Tests for Prompt 32: Retrieval Eval Static Report Page.

These tests cover the static report page generated by the
site builder for the Prompt 31 retrieval eval suite, the
verification script, and the Prompt 32 boundary guarantees.

The pattern follows the existing
``tests/test_prompt30_hybrid_retrieval.py`` and
``tests/test_prompt31_retrieval_eval.py`` tests: build a small
chunk index in a tmp dir using controlled records, point
``SiteBuilder`` at isolated data/repo directories, and assert on
the generated ``search/eval.md`` page and the verification
script's behavior.

The tests are grouped by responsibility:

- :class:`TestReportGeneration` — the page is generated by
  ``SiteBuilder.build`` and contains the required sections.
- :class:`TestReportDeterminism` — two back-to-back builds
  produce byte-identical reports.
- :class:`TestReportContent` — the report includes the
  required metric names, envelope fields, and per-mode/per-k
  summaries.
- :class:`TestReportDefensiveFallbacks` — the builder never
  fails when indexes are missing or the fixture is broken.
- :class:`TestVerificationScript` — the
  ``scripts/verify_retrieval_eval_report.py`` script accepts a
  ``--site-dir`` flag, exits 0 on a valid report, exits non-zero
  on a missing report, and is wired to the in-repo report path.
- :class:`TestBuildSiteAndValidate` — ``build-site`` regenerates
  the page; ``smoke-site`` and ``validate`` still pass.
- :class:`TestPrompt32Boundaries` — scope guards (no
  LLM/embedding/vector-DB dependencies; no Prompt 33 files; no
  context-pack or answer-generation work; BM25, vector, and
  chunk internals unmodified).
"""

